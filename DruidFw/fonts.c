  if (code >= '}' && code <= '~')
    return FONT_pt_mono_bold_8x13_1[code];

  if (code == 241) // ё
    return FONT_pt_mono_bold_8x13_2[code + 128];

  return FONT_pt_mono_bold_8x13_1['!'];
