const unsigned char* FONT_GetPicture(signed char code, Font font) {
  switch (font) {
    
  case nimbus_bold_16:
    if (code < 0) {
      code += 128;
      _log("nimbus 16: %d", code);
      return pgm_read_ptr_near( FONT_nimbus_bold_16_2 + code );
    } else {
      return pgm_read_ptr_near( FONT_nimbus_bold_16_1 + code );
    }

  case nimbus_mono_10:
    if (code < 0) {
      code += 256;
    //_log("nimbus 10: %d", code);
      return pgm_read_ptr_near( FONT_nimbus_mono_10_2 + code );
    } else {
      return pgm_read_ptr_near( FONT_nimbus_mono_10_1 + code );
    }

  default:
    CHECK(0);
    break;
  }

  return FONT_nimbus_bold_16_1[0];
}

byte FONT_GetHeight(Font font) {
  switch(font) {
    case nimbus_bold_16:
      return 23;
    case nimbus_mono_10:
      return 15;
    default:
      CHECK(0);
  }
  return 10;
}

byte FONT_GetWidth(Font font) {
  switch(font) {
    case nimbus_bold_16:
      return 12;
    case nimbus_mono_10:
      return 9;
    default:
      CHECK(0);
  }
  return 5;
}
