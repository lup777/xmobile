const byte FONT_max_width = 12;
const byte FONT_max_height = 24;

const uint8_t* FONT_GetPicture(byte code, Font font) {
  switch (font) {
  case nimbus_bold_16:
    if (code >= 1 && code <= 127)
      return FONT_nimbus_bold_16_1[code];
    else
      return FONT_nimbus_bold_16_1[1];
    break;

  case nimbus_mono_10:
    if (code >= 1 && code <= 127)
      return FONT_nimbus_mono_10_1[code];
    else
      return FONT_nimbus_mono_10_2[code];
    break;

  default:
    CHECK(0);
    break;
  }
}

byte FONT_GetHeight(Font font) {
  switch(font) {
    case nimbus_bold_16:
      return 24;
    case nimbus_mono_10:
      return 13;
    default:
      CHECK(0);
  }
  return 10;
}

byte FONT_GetWidth(Font font) {
  switch(font) {
    case nimbus_bold_16:
      return 12;
    case nimbus_mono_10:
      return 9;
    default:
      CHECK(0);
  }
  return 5;
}